function normalizeProfileRegionData(rawData) {
  if (!Array.isArray(rawData)) {
    return [];
  }

  return rawData
    .map((province) => {
      const provinceName = typeof province?.name === "string" ? province.name.trim() : "";
      const cityNodes = Array.isArray(province?.children) ? province.children : [];
      const cities = cityNodes
        .map((city) => {
          const cityName = typeof city?.name === "string" ? city.name.trim() : "";
          const districtNodes = Array.isArray(city?.children) ? city.children : [];
          const districts = districtNodes
            .map((district) => (typeof district?.name === "string" ? district.name.trim() : ""))
            .filter(Boolean);

          if (!cityName) {
            return null;
          }

          return { name: cityName, children: districts };
        })
        .filter(Boolean);

      if (!provinceName || !cities.length) {
        return null;
      }

      return { name: provinceName, children: cities };
    })
    .filter(Boolean);
}

