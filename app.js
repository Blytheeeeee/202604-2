function applyProfileRegionPickerSelectedIndex(scrollElement, nextIndex) {
  const listElement = scrollElement.querySelector(".profile-region-picker-list");

  if (!listElement) {
    return;
  }

  const previousIndex = Number.parseInt(scrollElement.dataset.selectedIndex || "-1", 10);

  if (previousIndex !== nextIndex) {
    if (previousIndex >= 0 && previousIndex < listElement.children.length) {
      listElement.children[previousIndex].classList.remove("is-selected");
    } else {
      const staleSelectedElement = listElement.querySelector(".profile-region-picker-option.is-selected");
      if (staleSelectedElement) {
        staleSelectedElement.classList.remove("is-selected");
      }
    }
  }

  if (nextIndex >= 0 && nextIndex < listElement.children.length) {
    listElement.children[nextIndex].classList.add("is-selected");
    scrollElement.dataset.selectedIndex = String(nextIndex);
    return;
  }

  scrollElement.dataset.selectedIndex = "-1";
}

function updateProfileRegionPickerSelection(scrollElement, commitHierarchy = true) {
  if (!scrollElement) {
    return;
  }

  const column = scrollElement.dataset.column || "province";
  const options = getProfileRegionPickerOptions(column);

  if (!options.length) {
    return;
  }

  const nextIndex = Math.max(
    0,
    Math.min(options.length - 1, Math.round(scrollElement.scrollTop / PROFILE_REGION_PICKER_OPTION_HEIGHT))
  );
  const nextValue = options[nextIndex];
  const previousIndex = Number.parseInt(scrollElement.dataset.selectedIndex || "-1", 10);
  const previousValue = getProfileRegionPickerDraftValue(column);

  if (previousIndex === nextIndex && previousValue === nextValue) {
    return;
  }

  if (commitHierarchy) {
    setProfileRegionPickerDraftValue(column, nextValue);
  } else {
    setProfileRegionPickerDraftValueWithoutSync(column, nextValue);
  }

  applyProfileRegionPickerSelectedIndex(scrollElement, nextIndex);
}

function scrollProfileRegionPickerToValue(column, value, behavior = "smooth", commitHierarchy = true) {
  const scrollElement = app.querySelector(`.profile-region-picker-scroll[data-column="${column}"]`);

  if (!scrollElement) {
    return;
  }

  const options = getProfileRegionPickerOptions(column);
  const targetIndex = options.indexOf(value);

  if (targetIndex === -1) {
    return;
  }

  if (commitHierarchy) {
    setProfileRegionPickerDraftValue(column, value);
  } else {
    setProfileRegionPickerDraftValueWithoutSync(column, value);
  }

  applyProfileRegionPickerSelectedIndex(scrollElement, targetIndex);
  scrollElement.scrollTo({
    top: targetIndex * PROFILE_REGION_PICKER_OPTION_HEIGHT,
    behavior
  });
}

function setupProfileRegionPicker() {
  const scrollElements = app.querySelectorAll(".profile-region-picker-scroll");

  if (!scrollElements.length) {
    return;
  }

  scrollElements.forEach((scrollElement) => {
    let snapTimer = 0;
    let isFrameQueued = false;

    scrollElement.addEventListener(
      "scroll",
      () => {
        if (!isFrameQueued) {
          isFrameQueued = true;
          window.requestAnimationFrame(() => {
            updateProfileRegionPickerSelection(scrollElement, false);
            isFrameQueued = false;
          });
        }

        window.clearTimeout(snapTimer);
        snapTimer = window.setTimeout(() => {
          const column = scrollElement.dataset.column || "province";
          const options = getProfileRegionPickerOptions(column);

          if (!options.length) {
            return;
          }

          const snappedIndex = Math.max(0, Math.min(options.length - 1, Math.round(scrollElement.scrollTop / PROFILE_REGION_PICKER_OPTION_HEIGHT)));

          scrollElement.scrollTo({
            top: snappedIndex * PROFILE_REGION_PICKER_OPTION_HEIGHT,
            behavior: "smooth"
          });

          setProfileRegionPickerDraftValue(column, options[snappedIndex]);
          applyProfileRegionPickerSelectedIndex(scrollElement, snappedIndex);

          if (column === "province" || column === "city") {
            syncProfileRegionPickerDependentColumns(column);
          }
        }, 90);
      },
      { passive: true }
    );
  });

  window.requestAnimationFrame(() => {
    scrollProfileRegionPickerToValue("province", appState.profileRegionDraftProvince, "auto");
    scrollProfileRegionPickerToValue("city", appState.profileRegionDraftCity, "auto");
    scrollProfileRegionPickerToValue("district", appState.profileRegionDraftDistrict, "auto");
  });
}

