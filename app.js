function clampSecondaryReturnButtonPosition(left, top, phoneElement, buttonElement) {
  const maxLeft = Math.max(0, phoneElement.clientWidth - buttonElement.offsetWidth);
  const maxTop = Math.max(0, phoneElement.clientHeight - buttonElement.offsetHeight);

  return {
    left: Math.max(0, Math.min(maxLeft, left)),
    top: Math.max(0, Math.min(maxTop, top))
  };
}

function persistSecondaryReturnPosition() {
  try {
    if (!appState.secondaryReturnPosition) {
      window.localStorage.removeItem(SECONDARY_RETURN_POSITION_STORAGE_KEY);
      return;
    }

    window.localStorage.setItem(
      SECONDARY_RETURN_POSITION_STORAGE_KEY,
      JSON.stringify(appState.secondaryReturnPosition)
    );
  } catch (error) {
    // Ignore storage failures.
  }
}

function setupSecondaryReturnButtonDrag() {
  const buttonElement = app.querySelector(".secondary-return-button");
  const phoneElement = app.querySelector(".phone");

  if (!buttonElement || !phoneElement) {
    return;
  }

  if (
    appState.secondaryReturnPosition &&
    Number.isFinite(appState.secondaryReturnPosition.left) &&
    Number.isFinite(appState.secondaryReturnPosition.top)
  ) {
    const clampedInitialPosition = clampSecondaryReturnButtonPosition(
      appState.secondaryReturnPosition.left,
      appState.secondaryReturnPosition.top,
      phoneElement,
      buttonElement
    );

    appState.secondaryReturnPosition = clampedInitialPosition;
    persistSecondaryReturnPosition();

    buttonElement.style.left = `${clampedInitialPosition.left}px`;
    buttonElement.style.top = `${clampedInitialPosition.top}px`;
    buttonElement.style.right = "auto";
    buttonElement.style.bottom = "auto";
  }

  let activePointerId = null;
  let startPointerX = 0;
  let startPointerY = 0;
  let startLeft = 0;
  let startTop = 0;
  let hasMoved = false;

  const resolveCurrentPosition = () => {
    const buttonRect = buttonElement.getBoundingClientRect();
    const phoneRect = phoneElement.getBoundingClientRect();

    return {
      left: buttonRect.left - phoneRect.left,
      top: buttonRect.top - phoneRect.top
    };
  };

  buttonElement.addEventListener("pointerdown", (event) => {
    if (event.button !== 0) {
      return;
    }

    event.preventDefault();
    activePointerId = event.pointerId;
    startPointerX = event.clientX;
    startPointerY = event.clientY;
    hasMoved = false;
    buttonElement.dataset.dragMoved = "0";

    const currentPosition = resolveCurrentPosition();
    startLeft = currentPosition.left;
    startTop = currentPosition.top;

    if (typeof buttonElement.setPointerCapture === "function") {
      buttonElement.setPointerCapture(activePointerId);
    }
  });

  buttonElement.addEventListener("pointermove", (event) => {
    if (activePointerId === null || event.pointerId !== activePointerId) {
      return;
    }

    const deltaX = event.clientX - startPointerX;
    const deltaY = event.clientY - startPointerY;

    if (!hasMoved && (Math.abs(deltaX) > 4 || Math.abs(deltaY) > 4)) {
      hasMoved = true;
      buttonElement.dataset.dragMoved = "1";
    }

    if (!hasMoved) {
      return;
    }

    const nextPosition = clampSecondaryReturnButtonPosition(
      startLeft + deltaX,
      startTop + deltaY,
      phoneElement,
      buttonElement
    );

    appState.secondaryReturnPosition = nextPosition;
    persistSecondaryReturnPosition();

    buttonElement.style.left = `${nextPosition.left}px`;
    buttonElement.style.top = `${nextPosition.top}px`;
    buttonElement.style.right = "auto";
    buttonElement.style.bottom = "auto";
  });

  const releasePointer = (event) => {
    if (activePointerId === null || event.pointerId !== activePointerId) {
      return;
    }

    if (
      typeof buttonElement.hasPointerCapture === "function" &&
      buttonElement.hasPointerCapture(activePointerId) &&
      typeof buttonElement.releasePointerCapture === "function"
    ) {
      buttonElement.releasePointerCapture(activePointerId);
    }

    activePointerId = null;
  };

  buttonElement.addEventListener("pointerup", releasePointer);
  buttonElement.addEventListener("pointercancel", releasePointer);

  buttonElement.addEventListener("click", (event) => {
    if (buttonElement.dataset.dragMoved === "1") {
      event.preventDefault();
      event.stopPropagation();
      buttonElement.dataset.dragMoved = "0";
    }
  });
}

function renderPage() {
  const currentPage = getCurrentPage();
  syncDocumentTitle(currentPage);

  if (currentPage === "profile-message") {
    markProfileMessagesAsRead();
  }

  if (currentPage !== "article-detail") {
    resetArticleDetailAudioState();
  }

  app.innerHTML = renderPhone(currentPage);
  setupSecondaryReturnButtonDrag();
  setupHomeWardPicker();

  if (currentPage === "profile-personal" && appState.profileBirthDatePickerOpen) {
    setupProfileBirthPicker();
  }

  if (currentPage === "profile-personal" && appState.profileRegionPickerOpen) {
    setupProfileRegionPicker();
  }

  if (currentPage === "profile-personal" && appState.profileGenderPickerOpen) {
    setupProfileGenderPicker();
  }

  if (currentPage === "profile-personal" && appState.profileRelationPickerOpen) {
    setupProfileRelationPicker();
  }

  if (currentPage === "profile-personal" && appState.profileMarkerGroupPickerOpen) {
    setupProfileMarkerGroupPicker();
  }

  if (currentPage === "profile-personal" && appState.profileEducationPickerOpen) {
    setupProfileEducationPicker();
  }

  if (currentPage === "search" && appState.searchScopePickerOpen) {
    setupSearchScopePicker();
  }

  if (currentPage === "search" && !appState.searchScopePickerOpen) {
    window.requestAnimationFrame(() => {
      const searchInput = app.querySelector(".search-page-input");

      if (!searchInput) {
        return;
      }

      searchInput.focus();
      const inputLength = searchInput.value.length;
      if (typeof searchInput.setSelectionRange === "function") {
        searchInput.setSelectionRange(inputLength, inputLength);
      }
    });
  }

  if (currentPage === "article-detail") {
    syncArticleDetailAudioUI();
  }
}

