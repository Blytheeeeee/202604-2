function getSearchResultEntries(keyword = appState.searchKeyword, scope = appState.searchScope) {
  const normalizedKeyword = normalizeSearchKeyword(keyword);
  const keywordTokens = normalizedKeyword.split(/\s+/).filter(Boolean);

  if (!keywordTokens.length) {
    return [];
  }

  const sourceItems = scope === "全部文章" ? articleListItems : articleListItems;

  return sourceItems
    .map((item, sourceIndex) => ({ item, sourceIndex }))
    .filter(({ item }) => {
      const searchableText = `${item.title} ${(item.tags || []).join(" ")}`.toLowerCase();
      return keywordTokens.every((token) => searchableText.includes(token));
    });
}

