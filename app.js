async function fetchWithTimeout(url, options = {}) {
  const requestController = new AbortController();
  const timeoutId = window.setTimeout(() => {
    requestController.abort();
  }, API_REQUEST_TIMEOUT_MS);

  try {
    return await fetch(url, {
      ...options,
      signal: requestController.signal
    });
  } finally {
    window.clearTimeout(timeoutId);
  }
}

async function fetchUserStateFromServer() {
  if (!appState.sessionUserId) {
    return null;
  }

  const endpoint = `${API_USER_STATE_ENDPOINT}?userId=${encodeURIComponent(appState.sessionUserId)}`;
  const response = await fetchWithTimeout(endpoint, {
    method: "GET",
    headers: {
      Accept: "application/json"
    }
  });

  if (!response.ok) {
    throw new Error(`Failed to fetch user state: ${response.status}`);
  }

  const payload = await response.json();
  return payload && typeof payload === "object" ? payload : null;
}

async function persistUserStateToServer(partialState) {
  if (!appState.sessionUserId || !partialState || typeof partialState !== "object") {
    return false;
  }

  const response = await fetchWithTimeout(API_USER_STATE_ENDPOINT, {
    method: "PUT",
    headers: {
      Accept: "application/json",
      "Content-Type": "application/json"
    },
    body: JSON.stringify({
      userId: appState.sessionUserId,
      ...partialState
    })
  });

  return response.ok;
}

async function hydrateUserStateFromServer() {
  let payload;

  try {
    payload = await fetchUserStateFromServer();
  } catch (error) {
    return false;
  }

  if (!payload || typeof payload !== "object") {
    return false;
  }

  let hasServerState = false;
  const profilePersonalData = payload.profilePersonalData;

  if (profilePersonalData && typeof profilePersonalData === "object") {
    applyProfilePersonalDataPayload(profilePersonalData);
    persistProfilePersonalDataToLocal(profilePersonalData);
    hasServerState = true;
  }

  if (Object.prototype.hasOwnProperty.call(payload, "profileMessageUnreadCount")) {
    const serverUnreadCount = Number.parseInt(payload.profileMessageUnreadCount, 10);
    if (Number.isFinite(serverUnreadCount)) {
      applyProfileMessageUnreadCount(serverUnreadCount);
      persistProfileMessageUnreadCountToLocal(appState.profileMessageUnreadCount);
      hasServerState = true;
    }
  }

  if (hasServerState) {
    renderPage();
  }

  return hasServerState;
}

