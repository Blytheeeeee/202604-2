function ensureProfileRegionDataLoaded() {
  if (profileRegionData.length) {
    return Promise.resolve(true);
  }

  if (profileRegionDataLoadPromise) {
    return profileRegionDataLoadPromise;
  }

  profileRegionDataLoadPromise = fetch(PROFILE_REGION_DATA_URL)
    .then((response) => {
      if (!response.ok) {
        throw new Error(`Failed to load region data: ${response.status}`);
      }
      return response.json();
    })
    .then((rawData) => {
      profileRegionData = normalizeProfileRegionData(rawData);

      if (!profileRegionData.length) {
        throw new Error("Region data is empty");
      }

      ensureProfileRegionCurrentHierarchy();
      resetProfileRegionDraftFromCurrent();
      return true;
    })
    .catch(() => {
      profileRegionData = [];
      return false;
    })
    .finally(() => {
      profileRegionDataLoadPromise = null;
    });

  return profileRegionDataLoadPromise;
}

